/**
 * 导出全局注册 element plus svg 图标
 * @param app vue 实例
 * @description 使用
 */
export function elSvg(app: App) {
	const icons = svg as any;
	for (const i in icons) {
		app.component(`ele-${icons[i].name}`, icons[i]);
	}
	app.component('SvgIcon', SvgIcon);
}
